// =============================================================================
// FILE        : wallace_mult_2x2.v
// DESCRIPTION : Explicit 2×2 unsigned Wallace tree multiplier
//
//   Partial products:
//     w0: a0·b0
//     w1: a0·b1 + a1·b0   (1 HA)
//     w2: a1·b1           (1 HA with carry from col 1)
//
//   Produces 4-bit product p[3:0] = a × b.
// =============================================================================

`default_nettype none
`timescale 1ns / 1ps

module wallace_mult_2x2 (
    input  wire [1:0] a,
    input  wire [1:0] b,
    output wire [3:0] p
);

    // -------------------------------------------------------------------------
    // Partial products
    // -------------------------------------------------------------------------
    wire pp00 = a[0] & b[0];
    wire pp01 = a[0] & b[1];
    wire pp10 = a[1] & b[0];
    wire pp11 = a[1] & b[1];

    // -------------------------------------------------------------------------
    // Stage 1: reduce column 1 (pp01, pp10) with 1 HA
    // -------------------------------------------------------------------------
    wire s1 = pp01 ^ pp10;
    wire c1 = pp01 & pp10;

    // -------------------------------------------------------------------------
    // Stage 2: reduce column 2 (pp11, c1) with 1 HA
    // -------------------------------------------------------------------------
    wire s2 = pp11 ^ c1;
    wire c2 = pp11 & c1;

    // -------------------------------------------------------------------------
    // Output bits
    // -------------------------------------------------------------------------
    assign p[0] = pp00;
    assign p[1] = s1;
    assign p[2] = s2;
    assign p[3] = c2;

endmodule

`default_nettype wire